package Secao2_Est_de_Controle;

public class For3 {

    public static void main(String[] args) {
      
        for(int i = 1; i < 10; i++) {
            for(int j = 0; j < 10; j ++) {
                System.out.printf("[%d %d]", i, j);
            }  
            System.out.println();
        }
    }
}



